public class ShouCangAdapter extends RecyclerView.Adapter {
    private Context context;
    private ArrayList<ShuJuKuDemo> list;

    public ShouCangAdapter(ArrayList<ShuJuKuDemo> list, FragmentActivity activity) {
        this.list = list;
        this.context = activity;
    }

    @NonNull
    @Override
    public RecyclerView.ViewHolder onCreateViewHolder(@NonNull ViewGroup viewGroup, int i) {
        View inflate = LayoutInflater.from(context).inflate(R.layout.item_shoucang, null, false);
        return new MyViewHolder(inflate);
    }

    @Override
    public void onBindViewHolder(@NonNull RecyclerView.ViewHolder viewHolder, final int i) {
        MyViewHolder viewHolder1 = (MyViewHolder) viewHolder;
        viewHolder1.txt.setText(list.get(i).getUrl());

        RequestOptions options = new RequestOptions().circleCrop();
        Glide.with(context).load(list.get(i).getUrl()).apply(options).into(viewHolder1.img);

        viewHolder1.itemView.setOnLongClickListener(new View.OnLongClickListener() {
            @Override
            public boolean onLongClick(View view) {
                if (onItemLongClickListener != null) {
                    onItemLongClickListener.onitemclick(i);
                }
                return false;
            }
        });
    }

    @Override
    public int getItemCount() {
        return list.size();
    }

    class MyViewHolder extends RecyclerView.ViewHolder {

        private final ImageView img;
        private final TextView txt;

        public MyViewHolder(@NonNull View itemView) {
            super(itemView);
            img = itemView.findViewById(R.id.item_shoucang_img);
            txt = itemView.findViewById(R.id.item_shoucang_txt);

        }
    }


    private OnItemLongClickListener onItemLongClickListener;

    public interface OnItemLongClickListener {
        void onitemclick(int position);
    }

    public void setOnItemLongClickListener(OnItemLongClickListener onItemLongClickListener) {
        this.onItemLongClickListener = onItemLongClickListener;
    }
}
